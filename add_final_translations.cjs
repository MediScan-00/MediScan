const insertMissing = (langBlockStr, missingData) => {
  let blockInfo = content.split("  // ── APP ──────────────────────────────");
  let header = "";
  if (blockInfo.length > 1) {
    header = "  // ── APP ──────────────────────────────\n";
  }
  
  let newEntries = Object.entries(missingData).map(([k,v]) => {
     if(!langBlockStr.includes(k + ':')) {
        return '  ' + k + ': "' + v.replace(/"/g, '\\"') + '",';
     }
     return null;
  }).filter(Boolean).join('\n');
  
  if (newEntries.length > 0) {
     return langBlockStr.replace(/\{/, '{\n' + newEntries + '\n');
  }
  return langBlockStr;
};

// Split content by language blocks roughly
// `en: {` ... `ar: {` etc.
let langs = content.match(/[a-z]{2}:\s*\{[\s\S]*?\n\s*\}(?=\,|\n\})|export const TRANSLATIONS/g) || [];

let newContent = content;

if (newContent.includes('en: {') && newContent.includes('ar: {')) {
   let enBlock = newContent.substring(newContent.indexOf('en: {'), newContent.indexOf('ar: {'));
   let arBlockStart = newContent.indexOf('ar: {');
   let frBlockStart = newContent.indexOf('fr: {');
   let arBlock = newContent.substring(arBlockStart, frBlockStart > arBlockStart ? frBlockStart : newContent.indexOf('};'));

   let updatedEn = insertMissing(enBlock, missingEn);
   let updatedAr = insertMissing(arBlock, missingAr);

   newContent = newContent.replace(enBlock, updatedEn).replace(arBlock, updatedAr);
}

fs.writeFileSync('src/lib/i18n.ts', newContent);
